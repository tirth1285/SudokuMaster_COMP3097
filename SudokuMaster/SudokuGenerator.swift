    static func randomPuzzle(for difficulty: Difficulty) -> SudokuPuzzle {
        let pool = puzzles.filter { $0.difficulty == difficulty }
        if let puzzle = pool.randomElement() {
            return puzzle
        }
        return puzzles[0]
    }

    static func puzzle(for id: String) -> SudokuPuzzle? {
        return puzzles.first { $0.id == id }
    }
